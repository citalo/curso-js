//Math (matemática)

let num1 = 9.554732;
console.log(Math.floor(num1)); //menor inteiro.
console.log(Math.ceil(num1)); //maior inteiro.
console.log(Math.round(num1)); //mais próximo inteiro.
console.log(Math.max(-10,-5,0,46,100,2,78)) //maior valor.
console.log(Math.min(-10,-5,0,46,100,2,78)) //menor valor.
console.log(Math.random()); //cria um valor no intervalo (0,1), 1 não está incluido.
console.log(Math.random()*(10-5)+5);// Gera um número aleatório entre 5 e 10.
const numInt = Math.round(Math.random()*6); //Gerar número random inteiro.
console.log(numInt);
console.log(Math.PI);
console.log(Math.pow(2,10));//2**10 = 1024 (mais simples)
console.log(9**(1/2)); //raiz quadrada.
console.log(100/0); //infinity - torna-se um limite, erro para matemática.
